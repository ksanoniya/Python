# identifiers in python
# Identifiers are names used to identify a variable, function, class, module, or other object.
# Rules for naming identifiers:
# 1. Must start with a letter (a-z, A-Z) or an underscore
# 2. Can contain letters, digits (0-9), and underscores
# 3. Cannot start with a digit
# 4. Case-sensitive (e.g., `myVar` and `myvar` are different)
# 5. Cannot be a reserved keyword (e.g., `if`, `else`,
#    `while`, `for`, `def`, etc.)
# 6. Cannot contain spaces or special characters (e.g., @, #, $, %, etc.)
# Examples of valid identifiers:
my_variable = 10
myVariable = 20
def my_function():
    return "Hello, World!"
class MyClass:
    pass   